#include "Mutex.h"

/******************************************************************************/
/* Implementacion */
/*------------------------------*/

Mutex::Mutex(){
    /* Inicializa el mutex */
    pthread_mutex_init(&this->mutex,NULL);
}
/*----------------------------------------------------------------------------*/
int Mutex::lock(){
    return pthread_mutex_lock(&this->mutex);
}
/*----------------------------------------------------------------------------*/
int Mutex::unlock(){
    return pthread_mutex_unlock(&this->mutex);	
}
/*----------------------------------------------------------------------------*/
Mutex::~Mutex(){
    pthread_mutex_destroy(&this->mutex);	
}
/*----------------------------------------------------------------------------*/
